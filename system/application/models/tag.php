<?php
class Tag{
	private $id;
	private $name;
	private $logo;
	private $q_total;

	public function __construct($data){
		list($this->id,$this->name,$this->logo,$this->q_total) = $data;
	}

	public function setId($id){
		$this->id = $id;
	}
	public function getId(){
		return $this->id;
	}

	public function setName($name){
		$this->name = $name;
	}
	public function getName(){
		return $this->name;
	}

	public function setLogo($logo){
		$this->logo = $logo;
	}
	public function getLogo(){
		return $this->logo;
	}

	public function setQ_total($q_total){
		$this->name = $q_total;
	}
	public function getQ_total(){
		return $this->q_total;
	}
}
